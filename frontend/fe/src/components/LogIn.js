import React, { useState } from 'react';
import { useNavigate } from 'react-router-dom';
import axios from 'axios';

const LogIn = ({ onLogin }) => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');
  const navigate = useNavigate();

  const handleSubmit = async (e) => {
    e.preventDefault();
    setError('');

    try {
      const response = await axios.post('http://localhost:6900/login', { email, password });
      console.log('Full login response:', response);
      console.log('Login response data:', response.data);

      if (response.data && response.data.message === 'Login successful') {
        // Check if token and userId are present in the response
        if (response.data.token && response.data.userId) {
          localStorage.setItem('token', response.data.token);
          onLogin({ token: response.data.token, userId: response.data.userId });
          console.log('Navigating to profile page');
          navigate('/profile');
        } else {
          console.error('Token or userId missing from response:', response.data);
          setError('Invalid server response. Token or userId missing.');
        }
      } else {
        console.error('Unexpected response format:', response.data);
        setError('Login failed. Unexpected server response.');
      }
    } catch (error) {
      console.error('Login error:', error);
      if (error.response) {
        console.error('Error response:', error.response.data);
        console.error('Error status:', error.response.status);
      }
      setError(error.response?.data?.error || 'An error occurred. Please try again.');
    }
  };

  return (
    <div>
      <h2>Login</h2>
      {error && <p style={{ color: 'red' }}>{error}</p>}
      <form onSubmit={handleSubmit}>
        <div>
          <label htmlFor="email">Email:</label>
          <input
            type="email"
            id="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            required
          />
        </div>
        <div>
          <label htmlFor="password">Password:</label>
          <input
            type="password"
            id="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            required
          />
        </div>
        <button type="submit">Login</button>
      </form>
    </div>
  );
};

export default LogIn;